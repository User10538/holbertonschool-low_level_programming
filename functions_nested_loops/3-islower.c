/**
 * print_alphabet_x10 - Prints the alphabet 10 times, in lowercase,
 * followed by a new line each time.
 *  * _islower - checks if a character is lowercase
 * @c: the character to check
 *
 * Return: 1 if c is lowercase, 0 is capital
 */
int _islower(int c)
{
	return (c >= 'a' && c <= 'z');
}

